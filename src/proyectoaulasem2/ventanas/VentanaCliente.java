    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String cedula = txtDocumento.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String celular = txtCelular.getText();
        if(Cliente.ClientesBD.containsKey(cedula)){
            JOptionPane.showMessageDialog(this, "El cliente con cedula: "+cedula+" ya esta registrado");
            return ;
        }
        Cliente c = new Cliente();
        c.nroDocumento = cedula;
        c.nombre = nombre;
        c.apellido = apellido;
        c.celular = celular;
        Cliente.ClientesBD.put(cedula, c);
        
        try {
            Almacenamiento.guardar(Cliente.ClientesBD);
            JOptionPane.showMessageDialog(this, "Cliente registrado con exito!!");
            LimpiarCampos();
        } catch (IOException error) {
            JOptionPane.showMessageDialog(this, error.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            String cedula = txtDocumento.getText();
            Cliente.ClientesBD = Almacenamiento.recuperar();
            if(Cliente.ClientesBD.containsKey(cedula)){
                Cliente c = Cliente.ClientesBD.get(cedula);
                txtNombre.setText(c.nombre);
                txtApellido.setText(c.apellido);
                txtCelular.setText(c.celular);
                
                btnMod.setEnabled(true);
                btnEliminar.setEnabled(true);
            }else{
                JOptionPane.showMessageDialog(this, "El cliente no existe");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if(txtDocumento.getText() == null || txtDocumento.getText().isEmpty()){
            String msj = "Para eliminar, primero tiene que buscar un cliente";
            JOptionPane.showMessageDialog(this, msj);
            LimpiarCampos();
            return ;
        }
        int opc = JOptionPane.showConfirmDialog(this, "Â¿Seguro que desea borrar el cliente?", "CONFIRMAR", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if(opc == JOptionPane.YES_OPTION){
            Cliente.ClientesBD.remove(txtDocumento.getText());
            try {
                Almacenamiento.guardar(Cliente.ClientesBD);
                JOptionPane.showMessageDialog(this, "Cliente eliminado con exito");
                LimpiarCampos();
            } catch (IOException error) {
                JOptionPane.showMessageDialog(this, error.getMessage());
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

